  IF ( CLTYPE .EQ. 'RS' ) THEN
     GM1 = GM1_RS
  ELSE IF ( CLTYPE .EQ. 'CV' ) THEN
     GM1 = GM1_CV
  END IF

  DISF = KB * ( KEQ * EXP( DH * TFAC ) )

  IF( G1 .eq. 1 ) DISF = DISF * ( GM1 )

  RETURN

END FUNCTION DISF

