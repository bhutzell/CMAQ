     &   +    3.5000D-01 * RXRAT(   606 )      ! CL+PAA=0.3500D+00*HO+HCL+...
     &   +                 RXRAT(   613 )      ! CL+ISON=HO+HCL+0.3500D+...
     &   +    6.5000D-01 * RXRAT(   622 )      ! CL+ACE=0.6500D+00*HO+...
     &   +    8.2000D-01 * RXRAT(   680 )      ! CLO+APIP2=0.8200D+00*HO+...
     &   +    8.2000D-01 * RXRAT(   682 )      ! CLO+APINP2=0.8200D+00*HO+...
     &   +    7.7000D-01 * RXRAT(   684 )      ! CLO+LIMP2=0.7700D+00*HO+...
     &   +    7.7000D-01 * RXRAT(   686 )      ! CLO+LIMNP2=0.7700D+00*HO+...
     &   +                 RXRAT(   733 )      ! HOBR=HO+BR
     &   +    2.0000D+00 * RXRAT(   769 )      ! BR+ISHP=0.2000D+01*HO+...
     &   +    6.5000D-01 * RXRAT(   777 )      ! BR+ACE=0.6500D+00*HO+...
     &   +                 RXRAT(   805 )      ! HOI=HO+I

      P4 = YC0( HO ) + P4 * DTC

c..Production of HO2 from OH ( r5,4 terms )
      R5_4 =                 RKI(    41 ) * YC ( O3          )   ! HO+O3=HO2
     &     +                 RKI(    50 )                        ! HO=HO2
     &     +                 RKI(    54 ) * YC ( H2O2        )   ! HO+H2O2=HO2
     &     +                 RKI(    65 ) * YC ( NO3         )   ! HO+NO3=HO2+NO2
     &     +                 RKI(    76 ) * YC ( SO2         )   ! HO+SO2=HO2+SULF+...
     &     +                 RKI(    77 ) * YC ( CO          )   ! HO+CO=HO2
     &     +    3.5000D-01 * RKI(    85 ) * YC ( ACE         )   ! HO+ACE=0.3500D+...
     &     +    5.3000D-01 * RKI(    86 ) * YC ( BEN         )   ! HO+BEN=0.5300D+...
     &     +    1.8000D-01 * RKI(    87 ) * YC ( TOL         )   ! HO+TOL=0.1800D+...
     &     +    1.7000D-01 * RKI(    88 ) * YC ( XYL         )   ! HO+XYL=0.1700D+...
     &     +    1.8000D-01 * RKI(    89 ) * YC ( EBZ         )   ! HO+EBZ=0.1800D+...
     &     +    3.0000D-01 * RKI(   101 ) * YC ( INALD       )   ! HO+INALD=0.3000D+...
     &     +                 RKI(   108 ) * YC ( HCHO        )   ! HO+HCHO=HO2+CO
     &     +                 RKI(   114 ) * YC ( HKET        )   ! HO+HKET=HO2+MGLY
     &     +                 RKI(   118 ) * YC ( GLY         )   ! HO+GLY=HO2+...
     &     +    5.2000D-01 * RKI(   120 ) * YC ( DCB1        )   ! HO+DCB1=0.5200D+...
     &     +    5.2000D-01 * RKI(   121 ) * YC ( DCB2        )   ! HO+DCB2=0.5200D+...
