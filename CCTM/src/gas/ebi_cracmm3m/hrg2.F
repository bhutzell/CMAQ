     &   +    1.0000D-02 * RXRAT(   601 )      ! CL+OP3=0.1000D-01*HO+HCL+...
     &   +    1.0000D-02 * RXRAT(   602 )      ! CL+OPB=0.1000D-01*HO+HCL+...
