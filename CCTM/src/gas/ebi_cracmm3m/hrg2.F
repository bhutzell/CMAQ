     &   +    2.0000D-02 * RXRAT(   410 )      ! FURAN+O3=0.2000D-01*HO+ALD
     &   +    3.6000D-01 * RXRAT(   526 )      ! STY+O3=0.3600D+00*HO+...
     &   +                 RXRAT(   537 )      ! HOCL=HO+CL
     &   +                 RXRAT(   552 )      ! CL+HOCL=HO+CL2
     &   +    3.5000D-01 * RXRAT(   599 )      ! CL+OP1=0.3500D+00*HO+HCL+...
