     &   +    3.0000D-01 * RXRAT(   212 )      ! MVKP+NO=0.3000D+00*HO2+...
     &   +                 RXRAT(   213 )      ! UALP+NO=HO2+NO2+0.6100D+...
     &   +                 RXRAT(   216 )      ! ADDC+NO=HO2+NO2+0.3200D+...
     &   +                 RXRAT(   218 )      ! ORAP+NO=HO2+GLY+NO2
     &   +                 RXRAT(   219 )      ! OLNN+NO=HO2+NO2+ONIT
     &   +    7.4000D-01 * RXRAT(   264 )      ! MO2+MO2=0.7400D+00*HO2+...
