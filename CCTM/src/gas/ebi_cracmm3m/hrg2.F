     &     +    6.1900D-01 * RKI(   124 ) * YC ( PHEN        )   ! HO+PHEN=0.6190D+...
     &     +    5.8400D-01 * RKI(   125 ) * YC ( CSL         )   ! HO+CSL=0.5840D+...
     &     +                 RKI(   127 ) * YC ( MOH         )   ! HO+MOH=HO2+HCHO
     &     +                 RKI(   128 ) * YC ( EOH         )   ! HO+EOH=HO2+ACD
     &     +                 RKI(   129 ) * YC ( ROH         )   ! HO+ROH=HO2+...
     &     +                 RKI(   130 ) * YC ( ETEG        )   ! HO+ETEG=HO2+ALD
     &     +                 RKI(   136 ) * YC ( ORA1        )   ! HO+ORA1=HO2
     &     +    3.3300D-01 * RKI(   398 ) * YC ( BDE13       )   ! HO+BDE13=0.3330D+...
     &     +    4.9000D-01 * RKI(   406 ) * YC ( FURAN       )   ! HO+FURAN=0.4900D+...
     &     +    6.6000D-01 * RKI(   409 ) * YC ( FURANONE    )   ! HO+FURANONE=...
     &     +                 RKI(   412 ) * YC ( PROG        )   ! HO+PROG=HO2+...
     &     +    1.6000D-01 * RKI(   488 ) * YC ( VROCP6ARO   )   ! HO+VROCP6ARO=...
     &     +    1.6000D-01 * RKI(   494 ) * YC ( VROCP5ARO   )   ! HO+VROCP5ARO=...
     &     +    1.6000D-01 * RKI(   500 ) * YC ( NAPH        )   ! HO+NAPH=0.1600D+...
     &     +                 RKI(   568 ) * YC ( CLO         )   ! HO+CLO=HO2+CL
     &     +                 RKI(   748 ) * YC ( BRO         )   ! HO+BRO=HO2+BR
     &     +                 RKI(   796 ) * YC ( CH2BR2      )   ! HO+CH2BR2=HO2+BR+...
     &     +                 RKI(   859 ) * YC ( CH2BRCL     )   ! HO+CH2BRCL=HO2+CL+BR

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =    8.0000D-01 * RKI(     9 )                        ! HNO4=0.8000D+...
     &      +                 RKI(    74 )                        ! HNO4=HO2+NO2

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =    2.0000D+00 * RXRAT(    11 )      ! HCHO=0.2000D+01*HO2+CO
     &   +                 RXRAT(    12 )      ! ACD=HO2+MO2+CO
     &   +                 RXRAT(    13 )      ! ALD=HO2+ETHP+CO
     &   +    1.2200D+00 * RXRAT(    16 )      ! UALD=0.1220D+01*HO2+...
     &   +                 RXRAT(    17 )      ! PINAL=HO2+HC10P+CO
     &   +                 RXRAT(    18 )      ! LIMAL=HO2+HC10P+CO
     &   +                 RXRAT(    21 )      ! HKET=HO2+ACO3+HCHO
     &   +    6.6000D-01 * RXRAT(    22 )      ! MACR=0.6600D+00*HO2+...
     &   +    2.0000D+00 * RXRAT(    26 )      ! GLY=0.2000D+01*HO2+0.2000D+...
     &   +                 RXRAT(    27 )      ! MGLY=HO2+ACO3+CO
     &   +    1.5000D+00 * RXRAT(    28 )      ! DCB1=0.1500D+01*HO2+...
     &   +    1.5000D+00 * RXRAT(    29 )      ! DCB2=0.1500D+01*HO2+...
     &   +                 RXRAT(    30 )      ! BALD=HO2+CHO+CO
     &   +                 RXRAT(    31 )      ! OP1=HO2+HO+HCHO
     &   +                 RXRAT(    32 )      ! OP2=HO2+HO+ALD
     &   +                 RXRAT(    33 )      ! OPB=HO2+HO+ALD
     &   +                 RXRAT(    34 )      ! OP3=HO2+HO+ALD
     &   +                 RXRAT(    36 )      ! ONIT=HO2+NO2+0.2000D+...
     &   +    2.5000D-01 * RXRAT(    91 )      ! ISO+O3=0.2500D+00*HO2+...
     &   +    8.8000D-01 * RXRAT(    95 )      ! ISOP+NO=0.8800D+00*HO2+...
     &   +    3.4000D-01 * RXRAT(    97 )      ! ISHP+HO=0.3400D+00*HO2+...
     &   +    1.5000D-01 * RXRAT(   145 )      ! ETE+O3=0.1500D+00*HO2+...
     &   +    3.2000D-01 * RXRAT(   146 )      ! OLT+O3=0.3200D+00*HO2+...
     &   +    7.0000D-02 * RXRAT(   147 )      ! OLI+O3=0.7000D-01*HO2+...
