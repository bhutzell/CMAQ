      SUBROUTINE HRG2( DTC )
C**********************************************************************
C
C  FUNCTION: To solve for the concentration of HO, HO2, HONO, and
C            HNO4 alebraically.
C
C  PRECONDITIONS: For the CRACMM1_AQ mechanism
C
C  KEY SUBROUTINES/FUNCTIONS CALLED: None
C
C  REVISION HISTORY: Created by EBI solver program, Dec  3, 2021
C
C   18 Jul 14 B.Hutzell: revised to use real(8) variables
C**********************************************************************
      USE HRDATA

      IMPLICIT NONE


C..INCLUDES: None


C..ARGUMENTS:
      REAL( 8 ), INTENT( IN ) :: DTC                      ! Time step

C..PARAMETERS: None


C..EXTERNAL FUNCTIONS: NONE


C..SAVED LOCAL VARIABLES:
!     CHARACTER( 16 ), SAVE :: PNAME = 'HRG2'             ! Program name


C..SCRATCH LOCAL VARIABLES:
      REAL( 8 ) ::    O1D_S                 ! sum of O1D loss frequencies
      REAL( 8 ) ::    OH_S                  ! stoich coeff for OH from O1D+H2O or H2
      REAL( 8 ) ::    HO2_S                 ! stoich coeff for HO2 from O1D+H2 rxn
      REAL( 8 ) ::    XOH_S                 ! stoich coeff for OH & HONO from NO2EX loss rxn
      REAL( 8 ) ::    R4_19                 ! production of OH from HONO
      REAL( 8 ) ::    R19_4                 ! production of HONO from OH
      REAL( 8 ) ::    R4_5                  ! production of OH from HO2
      REAL( 8 ) ::    R5_4                  ! production of HO2 from OH
      REAL( 8 ) ::    R5_21                 ! production of HO2 from HNO4
      REAL( 8 ) ::    R21_5                 ! production of HNO4 from HO2
      REAL( 8 ) ::    P4, P5, P19, P21      ! species production form other paths
      REAL( 8 ) ::    L4, L5, L19, L21      ! species loss terms

      REAL( 8 ) ::    A, B, C               ! coeffs of quadratic eq. for HO2
      REAL( 8 ) ::    Q                     ! intermediate term

      REAL( 8 ) ::    T1, T2, T3            ! intermediate terms

      REAL( 8 ) ::    L21_INV               ! reciprocal of HNO4 loss term

C**********************************************************************


c..stoichiometric coefficient for production of HOX from O3 via O1D
      O1D_S =                 RKI(    44 )                      ! O1D=O3P
     &      +                 RKI(    45 )                      ! O1D=O3P
     &      +                 RKI(    46 )                      ! O1D=0.2000D+01*HO

      OH_S =    2.0000D+00 * RKI(    46 )                      ! O1D=0.2000D+01*HO

      OH_S  = OH_S / O1D_S


c..Production of HO from HO2 (r4,5 terms )
      R4_5 =                 RKI(    39 ) * YC ( O3        )   ! HO2+O3=HO
     &     +                 RKI(    54 ) * YC ( NO        )   ! HO2+NO=HO+NO2
     &     +    7.0000D-01 * RKI(    63 ) * YC ( NO3       )   ! HO2+NO3=0.7000D+...
     &     +    4.4000D-01 * RKI(   256 ) * YC ( ACO3      )   ! HO2+ACO3=0.4400D+...
     &     +    4.4000D-01 * RKI(   257 ) * YC ( RCO3      )   ! HO2+RCO3=0.4400D+...
     &     +    1.5000D-01 * RKI(   258 ) * YC ( ACTP      )   ! HO2+ACTP=0.1500D+...
     &     +    4.0000D-01 * RKI(   434 ) * YC ( FURANO2   )   ! HO2+FURANO2=...

      R4_5  = R4_5 * DTC

c..Production of HO from HONO (r4,19 terms )
      R4_19 =                 RKI(     7 )                      ! HONO=HO+NO

      R4_19 = R4_19 * DTC

c..Remaining HO production
      P4 =    2.0000D+00 * RXRAT(     3 )      ! H2O2=0.2000D+01*HO
     &   +                 RXRAT(     8 )      ! HNO3=HO+NO2
     &   +    2.0000D-01 * RXRAT(     9 )      ! HNO4=0.2000D+00*HO+0.8000D+...
     &   +    3.4000D-01 * RXRAT(    22 )      ! MACR=0.3400D+00*HO+0.6600D+...
     &   +                 RXRAT(    31 )      ! OP1=HO+HO2+HCHO
     &   +                 RXRAT(    32 )      ! OP2=HO+HO2+ALD
     &   +                 RXRAT(    33 )      ! OPB=HO+HO2+ALD
     &   +                 RXRAT(    34 )      ! PAA=HO+MO2
     &   +   OH_S  *       RXRAT(     2 )      ! O1D=0.2000D+01*HO
     &   +    8.0000D-02 * RXRAT(   132 )      ! ETE+O3=0.8000D-01*HO+...
     &   +    2.2000D-01 * RXRAT(   133 )      ! OLT+O3=0.2200D+00*HO+...
     &   +    4.6000D-01 * RXRAT(   134 )      ! OLI+O3=0.4600D+00*HO+...
     &   +    2.5000D-01 * RXRAT(   135 )      ! ISO+O3=0.2500D+00*HO+...
     &   +    8.5000D-01 * RXRAT(   136 )      ! API+O3=0.8500D+00*HO+...
     &   +    8.5000D-01 * RXRAT(   137 )      ! LIM+O3=0.8500D+00*HO+...
     &   +    4.0000D-02 * RXRAT(   138 )      ! LIMAL+O3=0.4000D-01*HO+...
     &   +    1.9000D-01 * RXRAT(   140 )      ! MACR+O3=0.1900D+00*HO+...
     &   +    1.6000D-01 * RXRAT(   141 )      ! MVK+O3=0.1600D+00*HO+...
     &   +    1.0000D-01 * RXRAT(   142 )      ! UALD+O3=0.1000D+00*HO+...
     &   +    5.0000D-02 * RXRAT(   143 )      ! DCB1+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   144 )      ! DCB2+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   145 )      ! DCB3+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT(   146 )      ! EPX+O3=0.5000D-01*HO+...
     &   +    5.0000D-01 * RXRAT(   164 )      ! EPX+NO3=0.5000D+00*HO+...
     &   +    2.8000D-01 * RXRAT(   167 )      ! TR2=0.2800D+00*HO+0.2900D+...
     &   +    4.9000D-01 * RXRAT(   168 )      ! TOLP=0.4900D+00*HO+0.1000D-...
     &   +    1.5800D-01 * RXRAT(   169 )      ! XY2=0.1580D+00*HO+0.3080D+...
     &   +    3.9000D-01 * RXRAT(   170 )      ! XYLP=0.3900D+00*HO+0.1000D-...
     &   +    1.5800D-01 * RXRAT(   171 )      ! XYO2=0.1580D+00*HO+0.3080D+...
     &   +    3.9000D-01 * RXRAT(   172 )      ! XYOP=0.3900D+00*HO+0.1000D-...
     &   +    8.2000D-01 * RXRAT(   199 )      ! APIP2+NO=0.8200D+00*HO+...
     &   +    8.2000D-01 * RXRAT(   201 )      ! APINP2+NO=0.8200D+00*HO+...
     &   +    7.7000D-01 * RXRAT(   203 )      ! LIMP2+NO=0.7700D+00*HO+...
     &   +    7.7000D-01 * RXRAT(   205 )      ! LIMNP2+NO=0.7700D+00*HO+...
     &   +    5.0000D-01 * RXRAT(   333 )      ! APIP2+ACO3=0.5000D+00*HO+...
     &   +    5.0000D-01 * RXRAT(   337 )      ! LIMP2+ACO3=0.5000D+00*HO+...
     &   +    5.0000D-01 * RXRAT(   339 )      ! LIMNP2+ACO3=0.5000D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   401 )      ! APIP2+APIP1=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   402 )      ! APIP2+LIMP1=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   403 )      ! APIP2+ISOP=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   404 )      ! LIMP2+APIP1=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   405 )      ! LIMP2+LIMP1=0.4800D+00*HO+...
     &   +    4.8000D-01 * RXRAT(   406 )      ! LIMP2+ISOP=0.4800D+00*HO+...
     &   +    2.0000D-02 * RXRAT(   436 )      ! FURAN+O3=0.2000D-01*HO+ALD

      P4 = YC0( HO ) + P4 * DTC

c..Production of HO2 from OH ( r5,4 terms )
      R5_4 =                 RKI(    38 ) * YC ( O3        )   ! HO+O3=HO2
     &     +                 RKI(    47 )                      ! HO=HO2
     &     +                 RKI(    51 ) * YC ( H2O2      )   ! HO+H2O2=HO2
     &     +                 RKI(    62 ) * YC ( NO3       )   ! HO+NO3=HO2+NO2
     &     +                 RKI(    73 ) * YC ( SO2       )   ! HO+SO2=HO2+SULF+...
     &     +                 RKI(    74 ) * YC ( CO        )   ! HO+CO=HO2
     &     +    4.9000D-02 * RKI(    79 ) * YC ( HC8       )   ! HO+HC8=0.4900D-...
     &     +    3.5000D-01 * RKI(    83 ) * YC ( ACE       )   ! HO+ACE=0.3500D+...
     &     +    6.4800D-01 * RKI(    84 ) * YC ( BEN       )   ! HO+BEN=0.6480D+...
     &     +    1.7700D-01 * RKI(    85 ) * YC ( TOL       )   ! HO+TOL=0.1770D+...
     &     +    1.7700D-01 * RKI(    86 ) * YC ( XYM       )   ! HO+XYM=0.1770D+...
     &     +    1.7700D-01 * RKI(    87 ) * YC ( XYE       )   ! HO+XYE=0.1770D+...
     &     +                 RKI(    93 ) * YC ( HCHO      )   ! HO+HCHO=HO2+CO
     &     +                 RKI(    99 ) * YC ( HKET      )   ! HO+HKET=HO2+MGLY
     &     +                 RKI(   103 ) * YC ( GLY       )   ! HO+GLY=HO2+...
     &     +    5.2000D-01 * RKI(   105 ) * YC ( DCB1      )   ! HO+DCB1=0.5200D+...
     &     +    5.2000D-01 * RKI(   106 ) * YC ( DCB2      )   ! HO+DCB2=0.5200D+...
     &     +    5.6000D-01 * RKI(   107 ) * YC ( DCB3      )   ! HO+DCB3=0.5600D+...
     &     +    6.1900D-01 * RKI(   109 ) * YC ( PHEN      )   ! HO+PHEN=0.6190D+...
     &     +    5.8400D-01 * RKI(   110 ) * YC ( CSL       )   ! HO+CSL=0.5840D+...
     &     +                 RKI(   111 ) * YC ( EPX       )   ! HO+EPX=HO2+XO2+...
     &     +                 RKI(   113 ) * YC ( MOH       )   ! HO+MOH=HO2+HCHO
     &     +                 RKI(   114 ) * YC ( EOH       )   ! HO+EOH=HO2+ACD
     &     +                 RKI(   115 ) * YC ( ROH       )   ! HO+ROH=HO2+...
     &     +                 RKI(   116 ) * YC ( ETEG      )   ! HO+ETEG=HO2+ALD
     &     +                 RKI(   122 ) * YC ( ORA1      )   ! HO+ORA1=HO2
     &     +    4.9000D-02 * RKI(   421 ) * YC ( ROCIOXY   )   ! HO+ROCIOXY=...
     &     +    4.9000D-01 * RKI(   432 ) * YC ( FURAN     )   ! HO+FURAN=0.4900D+...
     &     +    6.6000D-01 * RKI(   435 ) * YC ( FURANONE  )   ! HO+FURANONE=...
     &     +                 RKI(   438 ) * YC ( PROG      )   ! HO+PROG=HO2+...

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =    8.0000D-01 * RKI(     9 )                      ! HNO4=0.8000D+...
     &      +                 RKI(    71 )                      ! HNO4=HO2+NO2

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =    2.0000D+00 * RXRAT(    11 )      ! HCHO=0.2000D+01*HO2+CO
     &   +                 RXRAT(    12 )      ! ACD=HO2+MO2+CO
     &   +                 RXRAT(    13 )      ! ALD=HO2+ETHP+CO
     &   +    1.2200D+00 * RXRAT(    16 )      ! UALD=0.1220D+01*HO2+...
     &   +                 RXRAT(    17 )      ! PINAL=HO2+HC10P+CO
     &   +                 RXRAT(    18 )      ! LIMAL=HO2+HC10P+CO
     &   +                 RXRAT(    21 )      ! HKET=HO2+ACO3+HCHO
     &   +    6.6000D-01 * RXRAT(    22 )      ! MACR=0.6600D+00*HO2+...
     &   +    2.0000D+00 * RXRAT(    26 )      ! GLY=0.2000D+01*HO2+0.2000D+...
     &   +                 RXRAT(    27 )      ! MGLY=HO2+ACO3+CO
     &   +    1.5000D+00 * RXRAT(    28 )      ! DCB1=0.1500D+01*HO2+...
     &   +    1.5000D+00 * RXRAT(    29 )      ! DCB2=0.1500D+01*HO2+...
     &   +                 RXRAT(    30 )      ! BALD=HO2+CHO+CO
     &   +                 RXRAT(    31 )      ! OP1=HO2+HO+HCHO
     &   +                 RXRAT(    32 )      ! OP2=HO2+HO+ALD
     &   +                 RXRAT(    33 )      ! OPB=HO2+HO+ALD
     &   +                 RXRAT(    35 )      ! ONIT=HO2+NO2+0.2000D+...
     &   +    1.5000D-01 * RXRAT(   132 )      ! ETE+O3=0.1500D+00*HO2+...
     &   +    3.2000D-01 * RXRAT(   133 )      ! OLT+O3=0.3200D+00*HO2+...
     &   +    7.0000D-02 * RXRAT(   134 )      ! OLI+O3=0.7000D-01*HO2+...
     &   +    2.5000D-01 * RXRAT(   135 )      ! ISO+O3=0.2500D+00*HO2+...
     &   +    4.0000D-02 * RXRAT(   138 )      ! LIMAL+O3=0.4000D-01*HO2+...
     &   +    1.4000D-01 * RXRAT(   140 )      ! MACR+O3=0.1400D+00*HO2+...
     &   +    1.1000D-01 * RXRAT(   141 )      ! MVK+O3=0.1100D+00*HO2+...
     &   +    7.2000D-02 * RXRAT(   142 )      ! UALD+O3=0.7200D-01*HO2+...
     &   +                 RXRAT(   143 )      ! DCB1+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   144 )      ! DCB2+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT(   145 )      ! DCB3+O3=HO2+0.5000D-01*HO+...
     &   +    1.5000D+00 * RXRAT(   146 )      ! EPX+O3=0.1500D+01*HO2+...
     &   +                 RXRAT(   155 )      ! HCHO+NO3=HO2+CO+HNO3
     &   +                 RXRAT(   159 )      ! UALD+NO3=HO2+XO2+0.6680D+...
     &   +                 RXRAT(   160 )      ! GLY+NO3=HO2+0.2000D+01*CO+HNO3
     &   +    1.5000D+00 * RXRAT(   164 )      ! EPX+NO3=0.1500D+01*HO2+...
     &   +    2.9000D-01 * RXRAT(   167 )      ! TR2=0.2900D+00*HO2+0.2800D+...
     &   +    1.0000D-02 * RXRAT(   168 )      ! TOLP=0.1000D-01*HO2+...
     &   +    3.0800D-01 * RXRAT(   169 )      ! XY2=0.3080D+00*HO2+0.1580D+...
     &   +    1.0000D-02 * RXRAT(   170 )      ! XYLP=0.1000D-01*HO2+...
     &   +    3.0800D-01 * RXRAT(   171 )      ! XYO2=0.3080D+00*HO2+...
     &   +    1.0000D-02 * RXRAT(   172 )      ! XYOP=0.1000D-01*HO2+...
     &   +                 RXRAT(   181 )      ! MO2+NO=HO2+NO2+HCHO
     &   +                 RXRAT(   182 )      ! ETHP+NO=HO2+NO2+ACD
     &   +    6.6000D-01 * RXRAT(   183 )      ! HC3P+NO=0.6600D+00*HO2+...
     &   +    2.0000D-01 * RXRAT(   184 )      ! HC5P+NO=0.2000D+00*HO2+...
     &   +    6.0600D-01 * RXRAT(   185 )      ! HC8P+NO=0.6060D+00*HO2+...
     &   +                 RXRAT(   186 )      ! ETEP+NO=HO2+NO2+0.1600D+...
     &   +    7.8000D-01 * RXRAT(   187 )      ! OLTP+NO=0.7800D+00*HO2+...
     &   +    8.3000D-01 * RXRAT(   188 )      ! OLIP+NO=0.8300D+00*HO2+...
     &   +    9.1800D-01 * RXRAT(   189 )      ! BENP+NO=0.9180D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   191 )      ! TOLP+NO=0.9500D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   192 )      ! PER1+NO=0.5000D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   194 )      ! XYLP+NO=0.9500D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   195 )      ! PER2+NO=0.9500D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   196 )      ! XYOP+NO=0.9500D+00*HO2+...
     &   +    8.8000D-01 * RXRAT(   197 )      ! ISOP+NO=0.8800D+00*HO2+...
     &   +    8.2000D-01 * RXRAT(   198 )      ! APIP1+NO=0.8200D+00*HO2+...
     &   +    7.7000D-01 * RXRAT(   202 )      ! LIMP1+NO=0.7700D+00*HO2+...
     &   +    9.5000D-01 * RXRAT(   206 )      ! PINALP+NO=0.9500D+00*HO2+...
     &   +    9.4000D-01 * RXRAT(   207 )      ! LIMALP+NO=0.9400D+00*HO2+...
     &   +    6.7000D-01 * RXRAT(   211 )      ! MEKP+NO=0.6700D+00*HO2+NO2+...
     &   +    7.7000D-01 * RXRAT(   212 )      ! KETP+NO=0.7700D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   214 )      ! MCP+NO=0.5000D+00*HO2+NO2+...
     &   +    3.0000D-01 * RXRAT(   215 )      ! MVKP+NO=0.3000D+00*HO2+...
     &   +                 RXRAT(   216 )      ! UALP+NO=HO2+NO2+0.6100D+...
     &   +                 RXRAT(   219 )      ! ADDC+NO=HO2+NO2+0.3200D+...
     &   +                 RXRAT(   221 )      ! ORAP+NO=HO2+GLY+NO2
     &   +                 RXRAT(   222 )      ! OLNN+NO=HO2+NO2+ONIT
     &   +    7.4000D-01 * RXRAT(   273 )      ! MO2+MO2=0.7400D+00*HO2+...
     &   +                 RXRAT(   274 )      ! ETHP+MO2=HO2+0.7500D+...
     &   +    8.9400D-01 * RXRAT(   275 )      ! HC3P+MO2=0.8940D+00*HO2+...
     &   +    8.4200D-01 * RXRAT(   276 )      ! HC5P+MO2=0.8420D+00*HO2+...
     &   +    9.1000D-01 * RXRAT(   277 )      ! HC8P+MO2=0.9100D+00*HO2+...
     &   +                 RXRAT(   278 )      ! ETEP+MO2=HO2+0.1950D+...
     &   +                 RXRAT(   279 )      ! OLTP+MO2=HO2+0.1500D+...
     &   +                 RXRAT(   280 )      ! OLIP+MO2=HO2+0.7500D+...
     &   +    1.6000D+00 * RXRAT(   281 )      ! BENP+MO2=0.1600D+01*HO2+...
     &   +                 RXRAT(   282 )      ! TLP1+MO2=HO2+HCHO+BALD
     &   +    2.0000D+00 * RXRAT(   283 )      ! TOLP+MO2=0.2000D+01*HO2+...
     &   +    2.0000D+00 * RXRAT(   284 )      ! PER1+MO2=0.2000D+01*HO2+...
     &   +                 RXRAT(   285 )      ! XYL1+MO2=HO2+HCHO+BALD
     &   +    2.0000D+00 * RXRAT(   286 )      ! XYLP+MO2=0.2000D+01*HO2+...
     &   +    2.0000D+00 * RXRAT(   287 )      ! PER2+MO2=0.2000D+01*HO2+...
     &   +    2.0000D+00 * RXRAT(   288 )      ! XYOP+MO2=0.2000D+01*HO2+...
     &   +                 RXRAT(   289 )      ! ISOP+MO2=HO2+0.1310D+...
     &   +                 RXRAT(   290 )      ! APIP1+MO2=HO2+0.7500D+...
     &   +                 RXRAT(   291 )      ! APIP2+MO2=HO2+0.7500D+...
     &   +    5.0000D-01 * RXRAT(   292 )      ! APINP1+MO2=0.5000D+00*HO2+...
     &   +    7.5000D-01 * RXRAT(   293 )      ! APINP2+MO2=0.7500D+00*HO2+...
     &   +                 RXRAT(   294 )      ! LIMP1+MO2=HO2+0.7500D+...
     &   +                 RXRAT(   295 )      ! LIMP2+MO2=HO2+0.7500D+...
     &   +                 RXRAT(   296 )      ! LIMNP1+MO2=HO2+0.7500D+...
     &   +                 RXRAT(   297 )      ! LIMNP2+MO2=HO2+0.7500D+...
     &   +    9.0000D-01 * RXRAT(   298 )      ! ACO3+MO2=0.9000D+00*HO2+...
     &   +    9.0000D-01 * RXRAT(   299 )      ! RCO3+MO2=0.9000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT(   300 )      ! ACTP+MO2=0.5000D+00*HO2+...
     &   +    8.3400D-01 * RXRAT(   301 )      ! MEKP+MO2=0.8340D+00*HO2+...
     &   +                 RXRAT(   302 )      ! KETP+MO2=HO2+0.7500D+...
     &   +    5.0000D-01 * RXRAT(   303 )      ! MACP+MO2=0.5000D+00*HO2+...
